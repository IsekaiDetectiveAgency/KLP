fun main() {
    var num: Int = 0
    do {
        if (num < 3) println(num)
        else when {
            num % 3 ==0 && num % 5 == 0 -> println("FizzBuzz")
            num % 3 == 0 -> println("Fizz")
            num % 5 == 0 -> println("Buzz")
            else -> println(num)
        }
        num ++
    }while (num < 101)
}
